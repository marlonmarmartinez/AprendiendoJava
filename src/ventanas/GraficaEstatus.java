    @Override
    public void paint(Graphics g){
        GraficasEstatus cantidad=new GraficasEstatus();
       super.paint(g);
       if(bandera==true){
    
        int nuevoingreso=cantidad.nuevoIngreso();
        int noreparado=cantidad.noReparado();
        int enrevision=cantidad.enRevision();
        int reparado=cantidad.reparado();
        int entregado=cantidad.entregado();
        int otro=cantidad.otro();
        
        int valormayor=mayorValor(nuevoingreso, noreparado, enrevision, reparado, entregado, otro);
        
        int ningreso=nuevoingreso*400/valormayor;
        int nreparados=noreparado*400/valormayor;
        int erevision=enrevision*400/valormayor;
        int reparados=reparado*400/valormayor;
        int entregados=entregado*400/valormayor;
        int otros=otro*400/valormayor;
        
        g.setColor(new Color(255,170,0));
        g.fillRect(100, 100, ningreso, 40);
        g.drawString("Nuevo ingreso", 10, 118);
        g.drawString("Cantida "+nuevoingreso, 10, 133);
        
        g.setColor(new Color(255,0,0));
        g.fillRect(100, 150, nreparados, 40);
        g.drawString("No reparados", 10, 168);
        g.drawString("Cantida "+noreparado, 10, 180);
        
        g.setColor(new Color(0,0,255));
        g.fillRect(100, 200, erevision, 40);
        g.drawString("En revision", 10, 218);
        g.drawString("Cantida "+enrevision, 10, 233);
        
        g.setColor(new Color(0,150,255));
        g.fillRect(100, 250, reparados, 40);
        g.drawString("Reparados", 10, 268);
        g.drawString("Cantida "+reparado, 10, 280);
        
        g.setColor(new Color(0,130,0));
        g.fillRect(100, 300, entregados, 40);
        g.drawString("Entregados", 10, 318);
        g.drawString("Cantida "+entregado, 10, 333);
        
        g.setColor(new Color(150,100,100));
        g.fillRect(100, 350, otros, 40);
        g.drawString("Otro", 10, 368);
        g.drawString("Cantida "+otro, 10, 380);
        
       }
    }
    public int mayorValor(int nuevoingreso,int noreparado,int enrevision,int reparado,int entregado,int otro){
        if(nuevoingreso>noreparado && nuevoingreso>enrevision & nuevoingreso>reparado && nuevoingreso>entregado && nuevoingreso>otro){
            return nuevoingreso;
        }else if(noreparado>enrevision && noreparado>reparado && noreparado>entregado && noreparado>otro){
            return noreparado;
        }else if(enrevision>reparado && enrevision>entregado && enrevision>otro){
            return enrevision;
        }else if(reparado>entregado && reparado>otro){
            return reparado;
        }else if(entregado>otro){
            return entregado;
        }
        else{
            return otro;
        }
    }
