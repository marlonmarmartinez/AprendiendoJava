    public final void mostrarClientes(){
        DefaultTableModel model=new DefaultTableModel();
                
                model.addColumn("Id Cliente");
                model.addColumn("Nombre");
                model.addColumn("E - mail");
                model.addColumn("Telefono");
                model.addColumn("Direccion");
                jTable1.setModel(model);
                
                try {
                Object []datos=new Object[5];
                Connection cn=Conexion.conectar();
                Statement ps=cn.createStatement();
                ResultSet rs=ps.executeQuery("select id_clientes,nombre_clientes,mail_clientes,tel_clientes,dir_clientes from clientes");
                
                while(rs.next()){
                    for (int i = 0; i < 5; i++) {
                        datos[i]=rs.getString(1+i);
                    }
                  model.addRow(datos);
                }
                rs.close();
        }catch(SQLException e){
             JOptionPane.showMessageDialog(null,"error al conectar a la BBDD contacte al administrador: "+e.getMessage());
        }
    }
    
     private void setEventoMouseClicked()
    {
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
 
        @Override
        public void mouseClicked(MouseEvent e) {
        //tblEjemploMouseClicked(e);
         int row = jTable1.rowAtPoint(e.getPoint());
        if (row >= 0 && jTable1.isEnabled())
        {
            nomcli=jTable1.getValueAt(row,1).toString();
            idcliente=Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        }
            new InformacionDelCliente(nomcli,idcliente,username).setVisible(true);
            
        }
        });
        
    }
    
