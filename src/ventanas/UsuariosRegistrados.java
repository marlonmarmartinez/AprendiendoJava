    public final void mostrarUsuarios(){    
                
                DefaultTableModel model=new DefaultTableModel();
                model.addColumn("Id Usuario");
                model.addColumn("Nombre");
                model.addColumn("UserName");
                model.addColumn("Permisos");
                model.addColumn("Estatus");
                jTable1.setModel(model);
                
            try {
                Object []datos=new Object[5];
                Connection cn=Conexion.conectar();
                Statement ps=cn.createStatement();
                ResultSet rs=ps.executeQuery("select id_usuario,nombre_usuario,username,tipo_nivel,estatus from usuarios");
                
                while(rs.next()){
                    for (int i = 0; i < 5; i++) {
                        datos[i]=rs.getString(1+i);
                    }
                  model.addRow(datos);
                }
                rs.close();
        }catch(SQLException e){
             JOptionPane.showMessageDialog(null,"error al conectar a la BBDD contacte al administrador: "+e.getMessage());
        }
      
   }
    private void setEventoMouseClicked()
    {
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
 
        @Override
        public void mouseClicked(MouseEvent e) {
        //tblEjemploMouseClicked(e);
         int row = jTable1.rowAtPoint(e.getPoint());
        if (row >= 0 && jTable1.isEnabled())
        {
            cadena=jTable1.getValueAt(row,2).toString();
            idusuario=Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        }
            new InformacionDelUsuario(cadena,idusuario,username).setVisible(true);
            
        }
        });
        
    }
 
